
package tp3_ej2;

public class Vehiculo {
    private String patente, marca;

    public Vehiculo(String patente, String marca) {
        this.patente = patente;
        this.marca = marca;
    }

    public String getPatente() {
        return patente;
    }

    public String getMarca() {
        return marca;
    }
    
    
    
}
